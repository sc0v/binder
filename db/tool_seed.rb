# Seeds the database with tools and their tool types.
# Below, users can define the type of a tool with simple strings and this
# function will take care of creating the corresponding tool type if it
# doesn't already exist (in which case it will reference the existing tool type).
#
# It is the responsibility of the administrator to login afterwards to
# ensure there aren't any duplicate tool types (there shouldn't be as long as there
# aren't spelling errors below).
def generate_tools
  TOOLS.each do |tool|
    tool_type ||= ToolType.find_by_name(tool[:type].strip)
    tool_type ||= ToolType.create(name: tool[:type].strip)
    Tool.create(barcode: tool[:barcode], tool_type: tool_type, description: tool[:description])
  end
end

